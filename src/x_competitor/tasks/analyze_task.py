"""数据分析任务"""

from crewai import Agent, Task


def create_analyze_task(agent: Agent, product: str) -> Task:
    return Task(
        description=(
            f"基于上游情报收集员提供的「{product}」用户反馈原始数据，进行严格的结构化分析：\n\n"
            f"1. **数据清洗**：去除重复、无关和低质量内容，记录清洗前后数据量\n"
            f"2. **痛点提取**：识别用户反复提及的问题和不满\n"
            f"3. **分类归纳**：按硬件质量、软件体验、售后服务、性价比等维度分类\n"
            f"4. **频次统计**：统计每个痛点被独立用户提及的次数\n"
            f"5. **严重程度评估**：根据用户情绪强度和对核心功能的影响评估为高/中/低\n"
            f"6. **原文引用**：为每个痛点附上 2-3 条来自不同用户的代表性原文\n\n"
            f"**严格约束：**\n"
            f"- 只列出有至少 2 条独立用户反馈支撑的痛点\n"
            f"- 每个痛点必须附带真实用户原文引用，缺少原文的痛点不得列入\n"
            f"- 频次数字必须基于实际统计，不可估算或夸大\n"
            f"- 不得推测或编造上游数据中不存在的痛点"
        ),
        expected_output=(
            f"一份「{product}」的结构化痛点分析报告，包含：\n"
            f"- 产品名称、有效数据总量（清洗后）\n"
            f"- 痛点列表（按频次降序），每个痛点包含：分类、问题描述、出现频次、严重程度、2-3条用户原文引用\n"
            f"- 只列出有充分证据支撑的痛点，不含推测性内容"
        ),
        agent=agent,
    )